package com.joebrothers.jenkinstest.tmp;


public class TmpRecord {
    int a;
    String b;

    public TmpRecord(int a, String b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
